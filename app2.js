function cuadradoDosNumeros(){
  let num1 = prompt('ingrese el primer numero para calcular el cuadrado de ese numero')
  let num2 = prompt('ingrese el segundo numero para calcular el cuadrado de ese numero')
  let resultado = num1 * num1
  let resultado2 = num2 * num2
  alert(`el cuadrado de ${num1} es ${resultado}`)
  alert(`el cuadrado de ${num2} es ${resultado2}`)
}

cuadradoDosNumeros()
